#include "HW.h"
//--------------------------------------------------------
HW::HW(QObject *parent)
{
}
//--------------------------------------------------------
void HW::HW_INIT()
{
	wiringPiSetup();
	pinMode(NASOS, OUTPUT);
}
//--------------------------------------------------------
void HW::HW_ON()
{
	if (_Debug) qDebug() << "Try to nasos ON";
	digitalWrite(NASOS, HIGH);
	delay(300);
}
//--------------------------------------------------------
void HW::HW_OFF()
{
	if (_Debug) qDebug() << "Try to nasos OFF";
	digitalWrite(NASOS, LOW);
	delay(300);
}
